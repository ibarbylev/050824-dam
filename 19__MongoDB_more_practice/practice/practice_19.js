  [
    {
      $match: { fuelType: "Diesel" }
    },
    {
      $bucket: {
        groupBy: "$price",
        boundaries: [0, 20000, 30000, 40000, 60000, Infinity],
        default: "Other",
        output: {
          count: { $sum: 1 }
        }
      }
    },
    {
      $project: {
        _id: {
          $cond: [
            { $lt: ["$_id", 20000] }, "до 20000",
            { $and: [{ $gte: ["$_id", 20000] }, { $lt: ["$_id", 30000] }] }, "от 20000 до 30000",
            { $and: [{ $gte: ["$_id", 30000] }, { $lt: ["$_id", 40000] }] }, "от 30000 до 40000",
            { $and: [{ $gte: ["$_id", 40000] }, { $lt: ["$_id", 60000] }] }, "от 40000 до 60000",
            { $gte: ["$_id", 60000] }, "свыше 60000"
          ]
        },
        count: 1
      }
    },
    { $sort: { count: -1 } }
  ]
